int addnums(int op1, int op2)
{
return op1 + op2;
}

int subnums(int op1, int op2)
{
return op1 - op2;
}
int multnums(int op1, int op2)
{
return op1 * op2;
}
int divnums(int op1, int op2)
{
return op1 / op2;
}
int andnums(int op1, int op2)
{
return op1 & op2;
}
int ornums(int op1, int op2)
{
return op1 | op2;
}
int xornums(int op1, int op2)
{
return op1 ^ op2;
}
int lshiftnums(int op1, int op2)
{
return op1 << op2;
}
unsigned int rshiftnums(unsigned int op1, int op2)
{
return op1 >> op2;
}